//  Single Dimensional Array

#include<iostream>

using namespace std;

int main()
{
    /*
        initiate and print variable in C++ language.
    
    */
    int ArrayNum[5] = {12, 0, 20, 0, 34}; // Declare ArrayNumbers
    // Traversing array

    for (int  a = 0; a < 5; a++)
    {
        cout << ArrayNum[a] << endl; 
    }
}